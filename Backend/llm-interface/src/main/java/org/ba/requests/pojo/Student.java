package org.ba.requests.pojo;

public class Student {
    private String name;
    private String surname;
    private int maricle;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public int getMaricle() {
        return maricle;
    }

    public void setMaricle(int maricle) {
        this.maricle = maricle;
    }
}

